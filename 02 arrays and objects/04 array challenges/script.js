const arr = [1, 2, 3, 4, 5];

// adds a value to the end of the array
arr.push(6);
// adds a value to the beginning of the array
arr.unshift(0);
// reverses the array
arr.reverse();

console.log(arr);

const arr1 = [1, 2, 3, 4, 5];
const arr2 = [5, 6, 7, 8, 9, 10];

// removes a value to the end of the array
arr1.pop();

// concatenates 2 arrays
arr3 = [...arr1, ...arr2];

console.log(arr3);
