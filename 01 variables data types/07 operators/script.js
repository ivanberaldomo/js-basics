// 3. Comparison Operators
// equal value operator ==
let l;
l = 2;
console.log(l);
l = 2 == 2;
console.log(l);
l = 2 == 3;
console.log(l);
l = 2 == '2';
console.log(l);
l = '2' == 2;
console.log(l);

// equal type and value operator ===
let v;
v = 2;
console.log(v);
v = 2 === 2;
console.log(v);
v = 2 === 3;
console.log(v);
v = 2 === '2';
console.log(v);
v = '2' === 2;
console.log(v);

// not equal value operator !=
let o;
o = 2;
console.log(o);
o = 2 != 2;
console.log(o);
o = 2 != 3;
console.log(o);
o = 2 != '2';
console.log(o);
o = '2' != 2;
console.log(o);

// not equal type and value operator !==
let f;
f = 2;
console.log(f);
f = 2 !== 2;
console.log(f);
f = 2 !== 3;
console.log(f);
f = 2 !== '2';
console.log(f);
f = '2' !== 2;
console.log(f);

