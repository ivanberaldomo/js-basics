// 1. Arithmetic Operators
// addition
let x;
x = 5 + 5;
console.log(x);

// subtraction
let y;
y = 5 - 5;
console.log(y);

// multiplication
let z;
z = 5 * 5;
console.log(z);

// division
let a;
a = 5 / 5;
console.log(a);

// modulus %, remainder after a division
let b;
b = 7 % 5;
console.log(b);

// concatenation
let c;
c = 'Hello' + ' ' + 'World';
console.log(c);

// exponent
let n;
n = 2 ** 3;
console.log(n);

// increment
let t;
t = 1;
// t = t + 1; or
t++;
console.log(t);

// decrement
let d;
d = 1;
// d = d - 1; or
d--;
console.log(d);

